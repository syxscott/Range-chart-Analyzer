class Translator:
    def __init__(self, lang="zh"):
        self.lang = lang if lang in TRANSLATIONS else "zh"
        # Expose the full translation table so embedding UIs (e.g. the
        # HistoryDetailDialog's WebEngine page) can serialize it and
        # resolve keys with their own t() shim.
        self.translations = TRANSLATIONS

    def set_lang(self, lang):
        self.lang = lang if lang in TRANSLATIONS else "zh"

    def t(self, key):
        d = TRANSLATIONS.get(self.lang, {})
        if key in d:
            return d[key]
        if key in TRANSLATIONS["en"]:
            return TRANSLATIONS["en"][key]
        return key

