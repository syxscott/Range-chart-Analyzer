// ==================== i18n runtime ====================
let RCA_LANG = 'zh';

function rcaSetLang(lang) {
  if (!RCA_I18N[lang]) lang = 'zh';
  RCA_LANG = lang;
  rcaStoreSet(RCA_STORE.lang, lang);
  document.documentElement.setAttribute('lang', lang === 'zh' ? 'zh-CN' : lang);
}

// Translate a key. Falls back to English, then to the key itself.
function t(key) {
  const dict = RCA_I18N[RCA_LANG] || {};
  if (key in dict) return dict[key];
  if (RCA_I18N.en && key in RCA_I18N.en) return RCA_I18N.en[key];
  return key;
}

// Apply translations to all [data-i18n] / [data-i18n-ph] / [data-i18n-title]
// elements currently in the DOM.
function rcaApplyI18n(root) {
  const scope = root || document;
  scope.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  scope.querySelectorAll('[data-i18n-ph]').forEach((el) => {
    el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph')));
  });
  scope.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
  });
}
